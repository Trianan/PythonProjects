# 5.1 "T.I.Y."
# True statements:
print('Examples of True statements:\n')

a = 1
print('Is a == 1? I predict True.')
print(a == 1)

b = 2
print('\nIs b >= 2? I predict True.')
print(b >= 2)

c = 16
print('\nIs c < 20? I predict True.')
print(c < 20)

d = 'jim'
print("\nDoes d != 'bob'? I predict True.")
print(d != 'bob')

list_letters = ['a', 'b', 'c']
print("\nIs 'd' not in list_letters? I predict True.")
print('d' not in list_letters)


# False statements:
print('\nExamples of False statements:\n')

a = 1
